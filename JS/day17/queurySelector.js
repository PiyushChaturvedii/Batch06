// Select Element using querySelector

// Using queryselector we can access element with id and class both

const h1Heading = document.querySelector('#heading')
console.log(h1Heading
);

// fetch by className
const footer = document.querySelector('.footer')
console.log(footer);

// select first with classname
const navItems = document.querySelector('.nav-items')
console.log(navItems);


// select all element that has same classnames

const nav_Items = document.querySelectorAll('.nav-items')
console.log(nav_Items);
